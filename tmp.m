clc
clear
t = 0: .001:1;
b0 = (1-t).^3;
b1 = 3*t.*(1-t).^2;
b2 = 3*(1-t).*t.^2;
b3 = t.^3;
%% Randomly chosen control points
P0 = [0,0,0; 1,1,1; 2,2,2; 2,2,2; 1,1,1];
P1 = [0.3,0.4,0.2; 1.3,1.5,1; 2.6, 2.8,3; 2.5,1.8,1.5; 1.2,0.3,0.2];
P2 = [0.6,0.8,0.4; 1.6,1.8,1.5; 3.6,2.8,1.2; 3.5,1.8,1.4; 1.8,0.4,0.25];
P3 = [1,1,0.8; 2,2,1.2; 4,3,2; 4,2,2.3; 2,0.5,0.3];

for i=1:size(P0,1)
x = P0(i,1)*b0+P1(i,1)*b1+P2(i,1)*b2+P3(i,1)*b3;
y = P0(i,2)*b0+P1(i,2)*b1+P2(i,2)*b2+P3(i,2)*b3;
z = P0(i,3)*b0+P1(i,3)*b1+P2(i,3)*b2+P3(i,3)*b3;
figure(1)
plot3(z,x,y)
hold on
end
hold off
